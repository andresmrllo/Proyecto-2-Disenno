    private void botonCrearCuenta1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearCuenta1ActionPerformed
        // TODO add your handling code here:
        if( !validarNumTelefono(textoNumTelefono.getText()) 
                || !validarCorreo(textoEmail.getText())
                || !validarNombre(textoNombre.getText())
                || !validarNombre(textoApellidos.getText())
                )
        {
            mostrarMensaje("Correo invalido");
            if(!validarNumTelefono(textoNumTelefono.getText()))
            {
                mostrarMensaje("Numero de telefono invalido");
            }
            if(!validarNombre(textoNombre.getText()))
            {
                 mostrarMensaje("Nombre con problemas");
            }
            if(!validarNombre(textoApellidos.getText()))
            {
                 mostrarMensaje("Apellidos con problemas");
            }
        }
        mostrarPanelPrincipal();
    }//GEN-LAST:event_botonCrearCuenta1ActionPerformed

