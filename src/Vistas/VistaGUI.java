    private boolean validarCorreo(String correo)
    {
         Pattern pattern = Pattern.compile(patronCorreo);
 
        // Match the given input against this pattern
        Matcher matcher = pattern.matcher(correo.toLowerCase());
        return matcher.matches();
    };
    
    private boolean validarNumTelefono(String numTelefono)
    {
         Pattern pattern = Pattern.compile(patronNumTelefono);
 
        // Match the given input against this pattern
        Matcher matcher = pattern.matcher(numTelefono);
        return matcher.matches();
    };
    
    private boolean validarNombre(String nombre)
    {
         Pattern pattern = Pattern.compile(patronNombre);
 
        // Match the given input against this pattern
        Matcher matcher = pattern.matcher(nombre);
        return matcher.matches();
    };
    
